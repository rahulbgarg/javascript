console.log(`========= Using For Loop ==============`);
console.log(`Step 1 : ========= Numbers from 5 to 15 for loop ==============`);
for (let index = 5; index<=15; index++) {
    console.log(index);  
}
console.log(`Step 2 : ========= Numbers from 50 to 40 for loop ==============`);
for (let index = 50; index>=40; index--) {
    console.log(index);  
}
console.log(`Step 3 : ========= First 15 Odd Numbers for loop ==============`);
for (let index = 1; index<=15; index=index+2) {
    console.log(index);  
}
console.log(`Step 4 : ========= First 10 Even Numbers for loop ==============`);
for (let index = 0; index<=10; index=index+2) {
    console.log(index);  
}
console.log(`Step 5 : ========= Table Of 5 for loop ==============`);
for (let index = 5; index<=50; index=index+5) {
    console.log(index);  
}
console.log(`Step 6 : ========= Table Of 10 for loop ==============`);
for (let index = 10; index<=100; index=index+10) {
    console.log(index);  
}
console.log(`Step 7 : ========= Table Of 10 in reverse using for loop ==============`);
for (let index = 100; index>=10; index=index-10) {
    console.log(index);  
}