console.log(`========= Using While ==============`);

console.log(`Step 1 : ========= Numbers from 5 to 15 Using While ==============`);
var i=5;
while (i<=15) {
    console.log(i);
    i++;
}

console.log(`Step 2 : ========= Numbers from 50 to 40 Using While ==============`);
var i=50;
while (i>=40) {
    console.log(i);
    i--;
}

console.log(`Step 3 : ========= First 15 Odd Numbers Using While ==============`);
var i=1;
while (i<=15) {
    console.log(i);
    i=i+2;
}

console.log(`Step 4 : ========= First 10 Even Numbers Using While ==============`);
var i=0;
while (i<=10) {
    console.log(i);
    i=i+2;
}

console.log(`Step 5 : ========= Table Of 5 Using While ==============`);
var i=5;
while (i<=50) {
    console.log(i);
    i=i+5;
}
console.log(`Step 6 : ========= Table Of 10 Using While ==============`);
var i=10;
while (i<=100) {
    console.log(i);
    i=i+10;
}

console.log(`Step 7 : ========= Table Of 10 in reverse Using While ==============`);
var i=100;
while (i>=10) {
    console.log(i);
    i=i-10;
}
